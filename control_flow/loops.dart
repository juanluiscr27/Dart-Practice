// Loops
void main() {
  // for loop
  for (var i = 0; i < 10; i++) {
    print('count: $i');
  }

  // for in loop
  var numbers = [1, 2, 3, 4, 5];
  for (var num in numbers) {
    print('number: $num');
  }

  // while loop
  int conditionNum = 5;
  while (conditionNum > 0) {
    print('Conditional number: $conditionNum');
    conditionNum--;
  }
  // do while
}
